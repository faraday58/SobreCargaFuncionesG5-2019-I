using System;

namespace SobreCargaFuncionesG5_2019_I
{
    static class Funciones
    {
        public static int Suma(int operando1, int operando2)
        {
            return operando1 + operando2;
        }

        public static int Suma( int operando1, int operando2, int operando3)
        {
            return operando1 + operando2 + operando3;
        } 

        public static int Multiplicar(int operando4, int operando5 , int operando6)
        {
            return operando4 * operando5 * operando6;
        }
    }
}
